import { NextPage, GetStaticProps } from "next";
import Head from "next/head";
import { Blog, CategoryProps } from "../interfaces";
import { client } from "../libs/client";
import { Layout } from "../components/Layout";
import { perpage } from "../constants";
import { Posts } from "../components/Posts";

type Props = {
  blogs: Blog[];
  categories: CategoryProps[];
  totalCount: number;
};

const Home: NextPage<Props> = ({ blogs, categories, totalCount }) => {
  return (
    <div className="flex flex-col">
      <Head>
        <title>Hayakawa&apos;s Tech Blog</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex-1 bg-stone-100 block justify-center items-center font-Body">
        <Layout categories={categories}>
          <Posts totalCount={totalCount} blogs={blogs} />
        </Layout>
      </main>
    </div>
  );
};

export const getStaticProps: GetStaticProps = async () => {
  const blog = await client.get({
    endpoint: "blog",
    queries: {
      offset: 0,
      limit: perpage,
    },
  });
  const category = await client.get({ endpoint: "category" });

  return {
    props: {
      blogs: blog.contents as Blog[],
      categories: category.contents as CategoryProps[],
      totalCount: blog.totalCount as number,
    },
  };
};

export default Home;
